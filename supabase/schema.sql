-- profiles ─ extends auth.users with a writable display_name.
create table if not exists public.profiles (
  id            uuid primary key references auth.users(id) on delete cascade,
  display_name  text not null,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);

-- programs ─ a cloud-shared ProgramArrangement. Identified for invitation
-- by `share_code` (unique, generated by the create RPC).
create table if not exists public.programs (
  id              uuid primary key default gen_random_uuid(),
  name            text not null,
  share_code      text not null unique,
  schema_version  int  not null default 1,
  created_by      uuid references auth.users(id) on delete set null,
  created_at      timestamptz not null default now(),
  updated_at      timestamptz not null default now()
);

-- program_members ─ M:N program ↔ user with role.
-- Roles: owner (full control), editor (can write), viewer (read only).
--
-- The user_id column carries TWO foreign keys: one to auth.users (the real
-- identity) and one to profiles (so PostgREST can embed display_name via
-- `select=...,profiles!inner(display_name)` — without this second FK the
-- embed fails with "Could not find a relationship between 'program_members'
-- and 'profiles'"). The FK to profiles is safe because handle_new_user()
-- inserts the profile row in the same auth.users-insert trigger.
create table if not exists public.program_members (
  program_id   uuid not null references public.programs(id) on delete cascade,
  user_id      uuid not null references auth.users(id)      on delete cascade,
  role         text not null check (role in ('owner','editor','viewer')),
  joined_at    timestamptz not null default now(),
  primary key (program_id, user_id)
);

-- Add the second FK separately so re-runs against an existing DB still
-- attach the constraint to the already-created table.
do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'program_members_user_id_profiles_fkey'
  ) then
    alter table public.program_members
      add constraint program_members_user_id_profiles_fkey
      foreign key (user_id) references public.profiles(id) on delete cascade;
  end if;
end;
$$;

-- dances ─ one DanceProject as JSON. `name` is denormalised from
-- dance_json.name for fast listings; clients should keep it in sync.
create table if not exists public.dances (
  id          uuid primary key default gen_random_uuid(),
  program_id  uuid not null references public.programs(id) on delete cascade,
  name        text,
  dance_json  jsonb not null,
  updated_by  uuid references auth.users(id) on delete set null,
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now()
);

-- program_items ─ ordered slots in the arrangement; each picks one dance
-- and the MQTT command that triggers it.
create table if not exists public.program_items (
  id            uuid primary key default gen_random_uuid(),
  program_id    uuid not null references public.programs(id) on delete cascade,
  dance_id      uuid not null references public.dances(id)   on delete cascade,
  order_index   int  not null,
  mqtt_command  text not null,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);

-- custom_animations ─ one CustomAnimation entry per row.
create table if not exists public.custom_animations (
  id              uuid primary key default gen_random_uuid(),
  program_id      uuid not null references public.programs(id) on delete cascade,
  animation_json  jsonb not null,
  created_at      timestamptz not null default now(),
  updated_at      timestamptz not null default now()
);

-- export_settings ─ at most one ExportSettings per program (PK = program_id).
create table if not exists public.export_settings (
  program_id    uuid primary key references public.programs(id) on delete cascade,
  settings_json jsonb not null,
  updated_by    uuid references auth.users(id) on delete set null,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);

-- activity_log ─ append-only audit / coordination feed. Members can write
-- their own actions and read everyone's.
create table if not exists public.activity_log (
  id          uuid primary key default gen_random_uuid(),
  program_id  uuid not null references public.programs(id) on delete cascade,
  user_id     uuid references auth.users(id) on delete set null,
  action_type text not null,
  details     jsonb,
  created_at  timestamptz not null default now()
);



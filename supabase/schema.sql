-- Bumps `updated_at` on UPDATE for any table that wires this trigger.
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- 8-char share code from a confusable-free alphabet (no I/L/O/0/1).
-- Used by create_program_with_owner; collisions are retried in the RPC.
create or replace function public.generate_share_code()
returns text
language plpgsql
volatile
set search_path = public
as $$
declare
  alphabet text := 'ABCDEFGHJKMNPQRSTUVWXYZ23456789';
  result   text := '';
  i        int;
begin
  for i in 1..8 loop
    result := result || substr(alphabet, 1 + floor(random() * length(alphabet))::int, 1);
  end loop;
  return result;
end;
$$;

-- Auto-create a profile row whenever Supabase Auth creates a user
-- (anonymous or otherwise). The display_name is later overwritten by the
-- RPCs when the user actually picks one.
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, display_name)
  values (
    new.id,
    coalesce(nullif(new.raw_user_meta_data->>'display_name', ''), 'Guest')
  )
  on conflict (id) do nothing;
  return new;
end;
$$;



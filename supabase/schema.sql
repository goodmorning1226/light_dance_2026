-- ---------------------------------------------------------------------
-- 9. RPC functions
--    Both are SECURITY DEFINER so they can write to programs /
--    program_members / profiles even though the calling client only
--    has the anon role and would otherwise be blocked by RLS.
-- ---------------------------------------------------------------------

-- Creates a program, picks a unique share code (retries on collision),
-- and adds the caller as the owner. Returns the program id + share code
-- so the frontend can immediately route the user into the program.
create or replace function public.create_program_with_owner(
  program_name text,
  display_name text
)
returns table (program_id uuid, share_code text)
language plpgsql
security definer
set search_path = public
as $$
declare
  _user_id     uuid;
  _program_id  uuid;
  _share_code  text;
  _attempt     int := 0;
begin
  _user_id := auth.uid();
  if _user_id is null then
    raise exception 'Not authenticated';
  end if;
  if coalesce(trim(program_name), '') = '' then
    raise exception 'program_name is required';
  end if;

  -- Make sure the profile exists / is up to date.
  insert into public.profiles (id, display_name)
  values (_user_id, coalesce(nullif(trim(display_name), ''), 'Guest'))
  on conflict (id) do update set display_name = excluded.display_name;

  -- Generate a unique share code. Bound the retry loop so a degenerate
  -- collision storm can't hang the request.
  loop
    _attempt   := _attempt + 1;
    _share_code := public.generate_share_code();
    begin
      insert into public.programs (name, share_code, created_by)
      values (program_name, _share_code, _user_id)
      returning id into _program_id;
      exit;
    exception when unique_violation then
      if _attempt >= 8 then
        raise exception 'Could not allocate a unique share code after % attempts', _attempt;
      end if;
    end;
  end loop;

  insert into public.program_members (program_id, user_id, role)
  values (_program_id, _user_id, 'owner');

  insert into public.activity_log (program_id, user_id, action_type, details)
  values (
    _program_id,
    _user_id,
    'program_created',
    jsonb_build_object('name', program_name)
  );

  return query select _program_id, _share_code;
end;
$$;


-- Joins the caller into the program identified by share_code as an
-- editor (or no-ops with the current role if they're already a member).
-- Returns the program id and effective role so the frontend can route.
create or replace function public.join_program_by_share_code(
  input_share_code text,
  display_name     text
)
returns table (program_id uuid, role text)
language plpgsql
security definer
set search_path = public
as $$
declare
  _user_id        uuid;
  _program_id     uuid;
  _existing_role  text;
begin
  _user_id := auth.uid();
  if _user_id is null then
    raise exception 'Not authenticated';
  end if;
  if coalesce(trim(input_share_code), '') = '' then
    raise exception 'share_code is required';
  end if;

  -- Refresh / create the profile.
  insert into public.profiles (id, display_name)
  values (_user_id, coalesce(nullif(trim(display_name), ''), 'Guest'))
  on conflict (id) do update set display_name = excluded.display_name;

  -- Resolve the share code (case sensitive — generate_share_code
  -- always produces uppercase, so the frontend should upper() user input
  -- before calling, OR we could `upper()` here — keeping strict for now).
  select p.id into _program_id
  from public.programs p
  where p.share_code = input_share_code;

  if _program_id is null then
    raise exception 'Invalid share code';
  end if;

  select pm.role into _existing_role
  from public.program_members pm
  where pm.program_id = _program_id
    and pm.user_id    = _user_id;

  if _existing_role is null then
    insert into public.program_members (program_id, user_id, role)
    values (_program_id, _user_id, 'editor');
    _existing_role := 'editor';

    insert into public.activity_log (program_id, user_id, action_type, details)
    values (
      _program_id,
      _user_id,
      'member_joined',
      jsonb_build_object('display_name', display_name)
    );
  end if;

  return query select _program_id, _existing_role;
end;
$$;

-- Allow anon-signed-in clients to call the RPCs.
grant execute on function public.create_program_with_owner(text, text) to authenticated;
grant execute on function public.join_program_by_share_code(text, text) to authenticated;


-- ---------------------------------------------------------------------
-- 10. Realtime publication
--     Add tables to `supabase_realtime`. Wrapped in DO blocks so the
--     script is re-runnable — adding an already-published table raises
--     `duplicate_object`, which we silently swallow.
-- ---------------------------------------------------------------------
do $$
declare
  t text;
begin
  foreach t in array array[
    'programs',
    'program_members',
    'dances',
    'program_items',
    'custom_animations',
    'export_settings',
    'activity_log'
  ] loop
    begin
      execute format('alter publication supabase_realtime add table public.%I', t);
    exception when duplicate_object then
      -- already published; nothing to do
      null;
    when undefined_object then
      -- supabase_realtime publication doesn't exist yet (rare on a fresh
      -- self-hosted install). Surface the error clearly.
      raise exception 'Publication "supabase_realtime" not found. Create it first or enable Realtime in the Supabase dashboard.';
    end;
  end loop;
end;
$$;


-- ---------------------------------------------------------------------
-- 11. Replica identity FULL
--     With the default REPLICA IDENTITY, DELETE events expose only the
--     primary key. Realtime subscribers filtering by `program_id` would
--     then miss deletes (program_id is no longer in the WAL row). FULL
--     ships every column on UPDATE and DELETE so client-side
--     `program_id=eq.<X>` filters work for every event type.
--
--     Trade-off: WAL volume is higher, especially for `dances` whose
--     `dance_json` may be large.
-- ---------------------------------------------------------------------
alter table public.dances            replica identity full;
alter table public.program_items     replica identity full;
alter table public.custom_animations replica identity full;
alter table public.export_settings   replica identity full;
alter table public.program_members   replica identity full;
alter table public.activity_log      replica identity full;
-- programs is keyed by id and clients subscribe per-id, so DEFAULT is fine.


-- ---------------------------------------------------------------------
-- 12. Sanity grants
--     The anon role itself never needs direct table access — clients
--     authenticate first and get the `authenticated` role. Granting
--     usage on the schema is enough for RLS-controlled access.
-- ---------------------------------------------------------------------
grant usage on schema public to anon, authenticated;
grant select, insert, update, delete on all tables in schema public to authenticated;
-- New tables created later by this script will need:
--   alter default privileges in schema public grant ... to authenticated;
-- but we already granted on `all tables` above for the existing set.


-- ---------------------------------------------------------------------
-- 6. RLS helper functions
-- ---------------------------------------------------------------------
-- These wrap the program_members lookup with SECURITY DEFINER so RLS
-- policies that reference program_members do NOT recursively re-invoke
-- their own RLS check (which would deadlock the policy evaluation).

create or replace function public.is_program_member(_program_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.program_members
    where program_id = _program_id
      and user_id    = auth.uid()
  );
$$;

create or replace function public.get_program_role(_program_id uuid)
returns text
language sql
stable
security definer
set search_path = public
as $$
  select role
  from public.program_members
  where program_id = _program_id
    and user_id    = auth.uid()
  limit 1;
$$;

create or replace function public.can_edit_program(_program_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.program_members
    where program_id = _program_id
      and user_id    = auth.uid()
      and role in ('owner', 'editor')
  );
$$;


